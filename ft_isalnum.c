#include "libft.h"

//Returns 1 if c is a number or a letter. 

int	ft_isalnum(int c)
{	
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
		return (1);
	else
		return (0);
}
 